namespace SimpleMonopoly;

/// <summary>
/// The <c>Util</c> static class is just used to print messages to the console. 
/// </summary>
public static class Util
{
    /// <summary>
    /// The <c>Print</c> static method prints to console
    /// </summary>
    /// <param name="message">A string parameter to get the message to print to the console.</param>
    public static void Print(string message)
    {
        Console.WriteLine(message);
    }
}